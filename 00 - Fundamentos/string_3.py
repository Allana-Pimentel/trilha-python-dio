#Fatiamento de strings
nome = "Guilherme Arthur de Carvalho"

print(nome[0]) #Pega o primeiro
print(nome[-2]) #Pega o penultimo
print(nome[:9]) #Pega do 0 ao 9
print(nome[10:]) #Pega do 10 em diante
print(nome[10:16]) #Pega do 10 ao 16
print(nome[10:16:2]) #Pula de 2 em 2
print(nome[:]) #Copia a string
print(nome[::-1]) #Inverte a string
